from os import listdir

def load_dft_data(route):
    flag = True
    base = ''
    pot = ''
    with open(route, 'r') as dat_base:
        dat_base.readline()
        line = dat_base.readline()
        while line != '':
            if flag:
                base += line
            else:
                pot += line
            line = dat_base.readline()
            if '****' in line:
                flag = False
                dat_base.readline()
                dat_base.readline()
                line = dat_base.readline()
    return base, pot

